import { join, relative, extname } from 'node:path';
import { readdir, readFile, stat } from 'node:fs/promises';
import { safeWriteFile } from '../utils/fs-helpers.js';
import { logger } from '../utils/logger.js';

// ────────────────────────────────────────────
// Types
// ────────────────────────────────────────────

export interface CompareResult {
  originalDir: string;
  reconstructedDir: string;
  originalFileCount: number;
  reconstructedFileCount: number;
  matchedFiles: FileMatch[];
  unmatchedOriginal: string[];
  unmatchedReconstructed: string[];
  overallSimilarity: number;
}

export interface FileMatch {
  originalPath: string;
  reconstructedPath: string;
  similarity: number;
  originalLines: number;
  reconstructedLines: number;
  sharedSymbols: string[];
}

// ────────────────────────────────────────────
// Main entry point
// ────────────────────────────────────────────

export async function compareSources(
  originalDir: string,
  reconstructedDir: string,
): Promise<CompareResult> {
  logger.info('Collecting source files...');
  const originalFiles = await collectSourceFiles(originalDir);
  const reconstructedFiles = await collectSourceFiles(reconstructedDir);

  logger.info(`Original: ${originalFiles.length} files, Reconstructed: ${reconstructedFiles.length} files`);

  // Match files by class name
  const matches: FileMatch[] = [];
  const matchedOriginal = new Set<string>();
  const matchedReconstructed = new Set<string>();

  for (const origFile of originalFiles) {
    const origName = fileBaseName(origFile);
    const origContent = await readFile(join(originalDir, origFile), 'utf-8');

    for (const reconFile of reconstructedFiles) {
      const reconName = fileBaseName(reconFile);

      // Match on filename (class name)
      if (origName === reconName) {
        const reconContent = await readFile(join(reconstructedDir, reconFile), 'utf-8');
        const similarity = computeSimilarity(origContent, reconContent);
        const sharedSymbols = findSharedSymbols(origContent, reconContent);

        matches.push({
          originalPath: origFile,
          reconstructedPath: reconFile,
          similarity,
          originalLines: origContent.split('\n').length,
          reconstructedLines: reconContent.split('\n').length,
          sharedSymbols,
        });

        matchedOriginal.add(origFile);
        matchedReconstructed.add(reconFile);
        break;
      }
    }
  }

  const unmatchedOriginal = originalFiles.filter((f) => !matchedOriginal.has(f));
  const unmatchedReconstructed = reconstructedFiles.filter((f) => !matchedReconstructed.has(f));

  const overallSimilarity =
    matches.length > 0
      ? Math.round(matches.reduce((sum, m) => sum + m.similarity, 0) / matches.length)
      : 0;

  return {
    originalDir,
    reconstructedDir,
    originalFileCount: originalFiles.length,
    reconstructedFileCount: reconstructedFiles.length,
    matchedFiles: matches,
    unmatchedOriginal,
    unmatchedReconstructed,
    overallSimilarity,
  };
}

export async function writeCompareReport(outputPath: string, result: CompareResult): Promise<void> {
  const lines: string[] = [];

  lines.push('# Source Comparison Report');
  lines.push('');
  lines.push(`> Generated by apk-archeologist on ${new Date().toISOString()}`);
  lines.push('');

  lines.push('## Overview');
  lines.push('');
  lines.push(`| Metric | Value |`);
  lines.push(`|--------|-------|`);
  lines.push(`| Original files | ${result.originalFileCount} |`);
  lines.push(`| Reconstructed files | ${result.reconstructedFileCount} |`);
  lines.push(`| Matched pairs | ${result.matchedFiles.length} |`);
  lines.push(`| Unmatched (original) | ${result.unmatchedOriginal.length} |`);
  lines.push(`| Unmatched (reconstructed) | ${result.unmatchedReconstructed.length} |`);
  lines.push(`| **Overall similarity** | **${result.overallSimilarity}%** |`);
  lines.push('');

  if (result.matchedFiles.length > 0) {
    lines.push('## File Matches');
    lines.push('');
    lines.push('| Original | Reconstructed | Similarity | Orig Lines | Recon Lines | Shared Symbols |');
    lines.push('|----------|---------------|------------|------------|-------------|----------------|');
    for (const match of result.matchedFiles) {
      lines.push(
        `| ${match.originalPath} | ${match.reconstructedPath} | ${match.similarity}% | ${match.originalLines} | ${match.reconstructedLines} | ${match.sharedSymbols.length} |`,
      );
    }
    lines.push('');

    // Detailed per-file
    lines.push('## Detailed Comparisons');
    lines.push('');
    for (const match of result.matchedFiles) {
      lines.push(`### ${fileBaseName(match.originalPath)}`);
      lines.push('');
      lines.push(`- **Similarity:** ${match.similarity}%`);
      lines.push(`- **Original:** ${match.originalLines} lines → **Reconstructed:** ${match.reconstructedLines} lines`);
      if (match.sharedSymbols.length > 0) {
        lines.push(`- **Shared symbols:** \`${match.sharedSymbols.slice(0, 20).join('`, `')}\``);
      }
      lines.push('');

      const bar = renderBar(match.similarity);
      lines.push(`\`\`\`\n${bar}\n\`\`\``);
      lines.push('');
    }
  }

  if (result.unmatchedOriginal.length > 0) {
    lines.push('## Unmatched Original Files');
    lines.push('');
    lines.push('These files from the original source had no corresponding reconstructed file:');
    lines.push('');
    for (const f of result.unmatchedOriginal) {
      lines.push(`- ${f}`);
    }
    lines.push('');
  }

  if (result.unmatchedReconstructed.length > 0) {
    lines.push('## Unmatched Reconstructed Files');
    lines.push('');
    lines.push('These reconstructed files had no corresponding original:');
    lines.push('');
    for (const f of result.unmatchedReconstructed) {
      lines.push(`- ${f}`);
    }
    lines.push('');
  }

  await safeWriteFile(outputPath, lines.join('\n'));
}

