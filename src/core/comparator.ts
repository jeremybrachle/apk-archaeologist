async function collectSourceFiles(dir: string): Promise<string[]> {
  const files: string[] = [];
  await walkCollect(dir, dir, files);
  return files;
}

async function walkCollect(baseDir: string, currentDir: string, accumulator: string[]): Promise<void> {
  let entries;
  try {
    entries = await readdir(currentDir, { withFileTypes: true });
  } catch {
    return;
  }

  for (const entry of entries) {
    const fullPath = join(currentDir, entry.name);
    if (entry.isDirectory()) {
      await walkCollect(baseDir, fullPath, accumulator);
    } else if (entry.isFile()) {
      const ext = extname(entry.name).toLowerCase();
      if (['.java', '.kt', '.cs', '.xml', '.json'].includes(ext)) {
        accumulator.push(relative(baseDir, fullPath));
      }
    }
  }
}

function fileBaseName(filePath: string): string {
  const base = filePath.split(/[/\\]/).pop() ?? filePath;
  return base.replace(extname(base), '');
}

function computeSimilarity(a: string, b: string): number {
  const aTokens = tokenize(a);
  const bTokens = tokenize(b);

  if (aTokens.size === 0 && bTokens.size === 0) return 100;
  if (aTokens.size === 0 || bTokens.size === 0) return 0;

  let shared = 0;
  for (const token of aTokens) {
    if (bTokens.has(token)) shared++;
  }

  const union = new Set([...aTokens, ...bTokens]).size;
  return Math.round((shared / union) * 100);
}

function tokenize(content: string): Set<string> {
  // Extract identifiers >2 chars
  const matches = content.match(/\b[a-zA-Z_]\w{2,}\b/g);
  return new Set(matches ?? []);
}

function findSharedSymbols(a: string, b: string): string[] {
  const aSymbols = extractSymbols(a);
  const bSymbols = extractSymbols(b);
  const shared: string[] = [];

  for (const sym of aSymbols) {
    if (bSymbols.has(sym)) shared.push(sym);
  }

  return shared.sort();
}

function extractSymbols(content: string): Set<string> {
  const symbols = new Set<string>();

  // Class/method names
  const classRe = /(?:class|interface|enum)\s+(\w+)/g;
  let m;
  while ((m = classRe.exec(content)) !== null) symbols.add(m[1]);

  const methodRe = /(?:fun|void|int|boolean|String|float|double|long)\s+(\w+)\s*\(/g;
  while ((m = methodRe.exec(content)) !== null) symbols.add(m[1]);

  // Field/variable names
  const fieldRe = /(?:val|var|private|public|protected)\s+(?:\w+\s+)?(\w+)\s*[=:;]/g;
  while ((m = fieldRe.exec(content)) !== null) symbols.add(m[1]);

  // String constants (API paths etc.)
  const stringRe = /"(\/[\w/]+)"/g;
  while ((m = stringRe.exec(content)) !== null) symbols.add(m[1]);

  return symbols;
}

function renderBar(percent: number): string {
  const width = 40;
  const filled = Math.round((percent / 100) * width);
  const empty = width - filled;
  const bar = '█'.repeat(filled) + '░'.repeat(empty);
  return `[${bar}] ${percent}%`;
}
